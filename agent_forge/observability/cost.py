"""Token usage and cost tracking per agent run.

Provides :class:`CostTracker` to record per-call token usage and
estimated cost, plus helpers to print a rich run summary and persist
it to disk as ``summary.json``.
"""

from __future__ import annotations

import json
import sys
from collections import Counter
from dataclasses import dataclass, field
from datetime import UTC, datetime
from pathlib import Path
from typing import TYPE_CHECKING, Any

if TYPE_CHECKING:
    from agent_forge.agent.models import AgentRun
    from agent_forge.llm.base import TokenUsage

