# ``scripts/update_prices.py`` which fetches the community-maintained
# LiteLLM pricing database.  Run ``make update-prices`` to refresh.
#
# Fallback defaults are used when the vendored file is missing.

_PRICES_FILE = Path(__file__).with_name("model_prices.json")

